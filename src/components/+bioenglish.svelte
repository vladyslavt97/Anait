Anait won several prizes at International competitions, such as: • “Bluebird of
Happiness” in Simferopol, Ukraine (3rd prize, 2002) • “Silver String Music
Competition” in Kramatorsk, Ukraine (1st prize, 2004) • International String
Quartet Competition in Radom, Poland (3rd prize, 2011) together with the
Nocturnum-Quartet • 4th Brigitte Kempen Competition of the Sponsoring
Association of the Music College, Aachen, Germany for students at the Cologne
University of Music (2nd prize, 2017) • XXIII Premio Internazionale di Musica
Gaetano Zinetti (92 points out of 100, 2018) She also participated in several
festivals: • As a scholarship holder of the Richard Wagner Society of Charkiv,
she participated at the Bayreuth Festival, Germany where she presented a
contemporary composition of Denys Bocharov. • From 8.-20.8.2014 she played at
the Vilalte Music Festival in Corsavy, France as first violin in several
different groups, such as trios, quartets, and sextets. Anait participated in a
plethora of master courses of leading violinists where she played with
violinists such as Vladimir Astrachanzev, Aleksey Koshvanets, Natalya
Boyarskaya, Anatoly Bazhenov, Maxim Rusanov and Thomas Brandis. Further courses
included playing with Markus Placci and Burkhard Maiss. She also visited a
master course with the “Aizuri” quartet from the USA and the first violin of the
“Armida” quartet, Martin Funda, from Berlin. From August 2015 on, Anait is the
first violin of the D.U.R. Quartet in Bremen, Germany. Anait and her colleagues
are given instruments by the famous Italian violin maker Alessandro Cicillati
through the company “Violin Assets”. From October 2019 on, she began working as
deputy concert master at the “Neue Philharmonie”, Berlin. For her dedication,
Anait received scholarships from the German Programme for gifted students
(Deutschlandstipendium) from 2014 until 2015. Furthermore, she received a
scholarship from the Richard Wagner Society of Charkiv. Work experience: •
Concert Master at the Municipal Orchestra of Delmenhorst • Concert Master at the
Chamber Philharmonic Emsland • Section Leader at the Silk Road Symphony
Orchestra, Berlin • I Violin Tutti at “Neue Philharmonie”, Hamburg • I Violin
Tutti at “Hansa Philharmonie”, Hamburg • Deputy concert master at “Neue
Philharmonie”, Berlin • I Violin Tutti (temporary substitute) – Theater für
Niedersachsen, Hildesheim • I Violin Tutti (temporary substitute) – “Deutsches
Filmorchester Babelsberg”, Berlin • II Violin Tutti – Russian-German Music
Academy under supervision of Maestro Valery Gergiev, Berlin
